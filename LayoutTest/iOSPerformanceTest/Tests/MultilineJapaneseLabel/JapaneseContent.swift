import UIKit

struct JapaneseContent {
    let icon: UIImage
    let title: String
    let authorName: String
}

extension JapaneseContent {
    static func makeContents() -> [JapaneseContent] {
        let icons = IconGenerator.makeIcons()
        return (0..<100)
            .compactMap { _ in icons.randomElement() }
            .map { icon in
                return JapaneseContent(
                    icon: icon.image,
                    title: words.randomElement()!,
                    authorName: String(words.randomElement()!.prefix(Int.random(in: 4..<12)))
                )
        }
    }
}

